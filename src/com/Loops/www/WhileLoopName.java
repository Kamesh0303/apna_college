package com.Loops.www;

public class WhileLoopName {

	public static void main(String[] args) {

      int i =0;   // Start
      
      while (i<=10)  // condition till programme rub and end when i =10
      {
    	  System.out.println(i);
    	  
    	  i++;  // incremental updation i=i+1=, 0+1=1, 1+1=2,2+1=3 . . . 9+1=10
    	  
      }
          System.out.println("End of Programme");	
	}

}
